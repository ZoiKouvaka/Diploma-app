package myy803.diplomas_mgt_app_skeleton;

public class Grades
{
	private double impl;//implementation grade
	private double rep;//report grade
	private double pres;//presentation grade
	


	public double getImpl() {
		return impl;
	}

	public void setImpl(double impl) {
		this.impl = impl;
	}

	public double getPres() {
		return pres;
	}

	public void setPres(double pres) {
		this.pres = pres;
	}

	public double getRep() {
		return rep;
	}

	public void setRep(double rep) {
		this.rep = rep;
	}
}